        <p><span>第一条</span>申请人通过填写此表格申请2015届辅仁博雅常春藤盟校暑期项目（以下简称辅仁博雅）学生资格。</p>
        <p><span>第二条</span>申请材料将交由辅仁博雅招生委员会对申请人所填写的资料各方面进行全面考核，并最终决定是否给予申请人录取资格。</p>
        <p><span>第三条</span>若申请人获得录取资格，则享有免费参加辅仁博雅2015届三门讲座课程的听课权利，不包含住宿、饮食、及VIP私人项目。</p>
        <p><span>第四条</span>申请人若获得此录取资格，需要在5天内回复确认是否参加。逾期回复，辅仁博雅将是其自愿放弃入学资格，且不可于今年再次申请。</p>
        <p><span>第五条</span>申请人需要向辅仁博雅支付500元申请费，用于招生委员会对申请材料的审核费用。申请费为一次性费用，不可退回，不可追溯。</p>
        <p><span>第六条</span>申请人都享有辅仁博雅开课后，所拍摄的课程剪辑回放视频及其他辅仁教授所举办的公益讲座课程的试听机会，具体活动会由辅仁博雅具体通知。</p>
        <p><span>第七条</span>辅仁博雅将对申请人的所有资料保密，并仅用于辅仁博雅暑期项目的入学审核，将不泄露申请资料给第三方机构。</p>
        <p><span>第八条</span>申请人有权放弃申请奖学金，在名额有限的前提下，辅仁博雅会联系少部分申请人，给予他们参加项目的机会。</p>
        <p><span>第九条</span>若申请人希望参加哈佛超小型写作课或系主任研究项目的，请在相应选项中打勾。</p>
        <p><span>第十条</span>申请人在同意以上条款的基础上，报名辅仁博雅常春藤盟校暑期项目。</p>
