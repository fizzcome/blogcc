                            <br>
                            <p>    对于美国留学有意向的高中生及大学生提供一个了解美国大学教育及新课程的机会，辅仁博雅暑期学校邀请来自美国哈佛大学及哥伦比亚大学等常春藤盟校的院系主任及终身荣誉教授在暑假期间为学生们带来精品美国大学体验。自2012年来，辅仁博雅邀请近二十名常春藤盟校教授来到上海亲自执教。
                               </p><br> <b> 做辅仁四年来的感受？</b><br>
                            <p>
                                2011年11月，还在美国国会实习的我和一群美国大学同学聊天的时候，有人问，中国有那么多人想去哈佛读书，可是每年为什么只能有这么十几个？教育是奢侈品，但我们希望把他大众化，我们希望每一个希望学习世界顶级名校知识的中国人，都能得到这样的教育。一百年前，中国人提出“教育兴国” “事业强国”两条方针。如今，我和我的同学们认为依然如此。
                                于是在2011年末，我们与美国哈佛大学及哥伦比亚大学学系主任们一起在美国波士顿这个世界级学府云集的学术圣地注册成立了辅仁博雅国际教育集团，主办每年夏季在中国开设以常春藤盟校教授为主要教学力量的暑期课程，希望能以最惠民的价格，带给中国人最好的教育思考。
                                一转眼，四年过去了。我反思，教育到底是什么？
                                </p><br>
                            <p>
                                我想，是可能性。2006年，我曾经参加一次哈佛大学举办的讲座，当时还刚刚从微软公司离职加入谷歌中国的李开复老师在台上回忆当时他在美国读书时得到的感悟。他当时说的，与我如今得到的结论一样，就是教育所带来的多元化成功的可能性。
                                一个人，他可以以各种方式成功。企业家，音乐家，一个好妈妈，或者环游过世界，对得起自己内心的追求，都是一种成功。何必在乎一个维度的成就？我觉得，教育就是促使学生们在面对这个未知世界的时候，能静下心来认认真真的再思考一次，这辈子，他所追求的到底是什么？而通过教育，解释他们心中的疑惑，帮助他们拨云见日，鼓励他们找到自己的初心，我想，这才是我们当年办这家公司，在中国开办这个项目，最重要的价值。
                                今年，辅仁已经进入第四个年头，走向成熟，无论是办学的宗旨，还是治学的态度。这四年来，我们沿着长江一路向东，拜访过46所学校，17个省市，开设74场公益讲座，平均每场100人，我们与近1000个家庭有过深入的交谈。
                                </p><br>
                            <p>
                                为了能够将这种教育精神散播的更远，我们决定今年对所有被录取参加辅仁暑校课程的学生发放全额奖学金。所有申请者将公平竞争，获得最终这个免费就读辅仁暑校的机会。我们的标准，只有三个字，“可能性”。只要学生能证明，他有过人之处，哪怕是一个炙热的梦想，一首动听的歌，或是走遍山河的照片，都可能被我们录取。
                                今年，我们将期待全中国最丰富多彩的一群学生，为我们带来最美的一个夏天。
                            </p>
                            <br><p>
                                平川<br>
                                创始人<br>
                                辅仁博雅
                        </p>
